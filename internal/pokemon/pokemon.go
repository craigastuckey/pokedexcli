func GetPokemon(url string) (Pokemon, error) {
	res, err := http.Get(url)
	if err != nil {
		return Pokemon{}, fmt.Errorf("error fetching data: %w", err)
	}

	body, err := io.ReadAll(res.Body)
	if err != nil {
		return Pokemon{}, fmt.Errorf("error reading response body: %w", err)
	}
	if res.StatusCode != http.StatusOK {
		return Pokemon{}, fmt.Errorf("error: received non-OK HTTP status: %s", res.Status)
	}
	res.Body.Close()

	pokemon := UnmarshalData(body)

	return pokemon, nil
}

func UnmarshalData(data []byte) Pokemon {
	var pokemon Pokemon
	err := json.Unmarshal(data, &pokemon)
	if err != nil {
		fmt.Println("Error unmarshaling JSON:", err)
	}
	if pokemon.Level == 0 {
		pokemon.Level = 1
	}
	if pokemon.MoveList == nil {
		pokemon.MoveList = make([]move.Move, 4)
		AddLevelUpMove(&pokemon)
	}
	return pokemon
}

func MarshalData(pokemon Pokemon) []byte {
	data, err := json.Marshal(pokemon)
	if err != nil {
		fmt.Println("Error marshaling JSON:", err)
	}
	return data
}

func ThrowPokeball(pm Pokemon) bool {
	// Simulate a random chance of catching the Pokemon
	catchChance := 0.5 // 50% chance to catch
	if pm.BaseExperience > 100 {
		catchChance = 0.3 // Harder to catch if base experience is high
	}

	return rand.Float64() < catchChance
}

func GetStats(pokemon Pokemon) {
	fmt.Printf("Level: %d\n", pokemon.Level)
	fmt.Printf("Name: %s\nHeight: %d\nWeight: %d\n", pokemon.Name, pokemon.Height, pokemon.Weight)
	fmt.Printf("Stats:\n")
	for _, stat := range pokemon.Stats {
		fmt.Printf("  -%s: %d\n", stat.Stat.Name, stat.BaseStat)
	}
	fmt.Printf("Types:\n")
	for _, typ := range pokemon.Types {
		fmt.Printf("  - %s\n", typ.Type.Name)
	}
	fmt.Printf("Moves:\n")
	for _, move := range pokemon.MoveList {
		fmt.Printf("  - %s\n", move.Name)
	}
}

func GetParty(party *[]Pokemon) {
	for i, pm := range *party {
		if pm.Name != "" {
			fmt.Printf("%d. %s\n", i+1, pm.Name)
		} else {
			fmt.Printf("%d. Empty Slot\n", i+1)
		}
	}
}

func AddToParty(party *[]Pokemon, pm Pokemon) error {
	for i := 0; i < len(*party); i++ {
		if (*party)[i].Name == "" {
			(*party)[i] = pm
			fmt.Printf("%s has been added to your party\n", pm.Name)
			return nil
		}
	}
	return fmt.Errorf("party is full, cannot add %s", pm.Name)
}

func RemoveFromParty(party *[]Pokemon, name string) error {
	for i := 0; i < len(*party); i++ {
		if (*party)[i].Name == name {
			(*party)[i] = Pokemon{}
			fmt.Printf("%s has been removed from your party\n", name)
			return nil
		}
	}
	fmt.Println("That Pokemon is not in your party.")
	return nil
}

func AddLevelUpMove(pokemon *Pokemon) error {
	var err error
	for _, m := range pokemon.Moves {
		if m.VersionGroupDetails[0].MoveLearnMethod.Name == "level-up" && m.VersionGroupDetails[0].LevelLearnedAt == pokemon.Level {
			for i := 0; i < len(pokemon.MoveList); i++ {
				if pokemon.MoveList[i].Name == "" {
					pokemon.MoveList[i], err = move.GetMove(m.Move.URL)
					if err != nil {
						return fmt.Errorf("error fetching move data: %w", err)
					}
					break
				}
				if i == len(pokemon.MoveList)-1 {
					fmt.Printf("%s already knows 4 moves, cannot learn %s\n", pokemon.Name, m.Move.Name)
					return fmt.Errorf("%s already knows 4 moves, cannot learn %s", pokemon.Name, m.Move.Name)
				}
			}
		}
	}

	return nil
}
